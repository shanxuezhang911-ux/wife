/**
 * 根据当前时间获取一个随机切入情景
 * @returns {string} 场景描述（让AI根据人设自行发挥）
 */
export function getOpeningLine() {
  const hour = new Date().getHours()
  let pool
  if (hour >= 6 && hour < 10) pool = SCENES.MORNING
  else if (hour >= 10 && hour < 14) pool = SCENES.NOON
  else if (hour >= 14 && hour < 18) pool = SCENES.AFTERNOON
  else if (hour >= 18 && hour < 23) pool = SCENES.EVENING
  else pool = SCENES.NIGHT
  return pool[Math.floor(Math.random() * pool.length)]
}

export function getMoodLabel() {
  const hour = new Date().getHours()
  if (hour >= 6 && hour < 10) return '起床气'
  if (hour >= 10 && hour < 14) return '做饭中'
  if (hour >= 14 && hour < 18) return '干家务'
  if (hour >= 18 && hour < 23) return '求你哄'
  return '要崩溃'
}
